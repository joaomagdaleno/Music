/// Representa os diferentes sistemas (personas) do aplicativo de música.
enum AppPersona {
  /// Foco em gerenciamento de arquivos, tags e organização local (Estilo Mp3Tag).
  librarian,

  /// Foco em consumo, streaming e playlists (Estilo Spotify).
  listener,

  /// Foco em entretenimento, karaoke e visualizadores (Estilo Party Machine).
  host,

  /// Foco em ferramentas utilitárias, edição de toques e segurança (Estilo Audio Lab).
  artisan,
}
